package com.example.actionstest

class Calculation {
    fun addTwoNumbers(a: Int, b: Int): Int {
        return a + b
    }

    fun subtractTwoNumbers(a: Int, b: Int): Int {
        return a - b
    }

    fun multiplyTwoNumbers(a: Int, b: Int): Int {
        return a * b
    }

    fun divideTwoNumbers(a: Int, b: Int): Int {
        return a / b
    }

    fun calculatePercentage(a: Double): Double {
        return a / 100
    }
}